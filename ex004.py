a = input('Digite algo: ')
print('O tipo primitivo é', type(a))
print('\033[31mSomente letras:\033[m \033[033m{}\033[m'.format(a.isalpha()))
print('\033[31mSomente números:\033[m \033[33m{}\033[m'.format(a.isnumeric()))
print('\033[31mNúmeros e letras:\033[m \033[33m{}\033[m'.format(a.isalnum()))
print('\033[31mSomente Espaços:\033[m \033[33m{}\033[m'.format(a.isspace()))
print('\033[31mSomente maiúsculas:\033[m \033[33m{}\033[m'.format(a.isupper()))
print('\033[31mSomente minúsculas:\033[m \033[33m{}\033[m'.format(a.islower()))
print('\033[31mEstá capitalizado:\033[m \033[33m{}\033[m'.format(a.istitle()))